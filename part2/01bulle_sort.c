#include <stdio.h>

/**
 * 冒泡排序函数
 * @param a 待排序的整型数组
 * @param n 数组的大小
 */

void bubble_sort(int a[], int n)
{
    for (int i = 0; i < n - 1; i++)           // 控制趟数
    {
        for (int j = 0; j < n - i - 1; j++)   // 每趟比较的范围
        {
            if (a[j] > a[j + 1])                  // ✅ 相邻元素比较！
            {   // 升序：前面 > 后面，就交换
                int temp = a[j];
                a[j] = a[j + 1];
                a[j + 1] = temp;
            }
        }
    }
}

// 🔵外层循环 for (i = 0; tug i < n - 1; i++)

// 作用	说明
// 控制趟数	一共需要 n - 1 趟
// 每趟确定一个元素的位置	第 i 趟结束后，最大的元素会被冒到数组末尾的第 i 个位置
// 类比：像气泡从水底往上冒，每趟冒一个最大的"气泡"到水面。


// 🔵 内层循环 for (j = 0; j < n - i - 1; j++)

// 设计要点	为什么这样设计
// j < n - i - 1	每趟结束后，末尾 i 个元素已经有序，不需要再比较
// 范围逐渐缩小	第 i 趟时，只需比较到 n - i - 1 的位置